package ejercicios.ejercicioOrdenDeCompra;

public class Cliente {

    private String nombre;
    private String aoellido;

    public Cliente(String nombre, String aoellido) {
        this.nombre = nombre;
        this.aoellido = aoellido;
    }

    public String getNombre() {
        return nombre;
    }

    public String getAoellido() {
        return aoellido;
    }

}
